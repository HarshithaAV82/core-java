class Blanket{
    String brand;
    String color;
    String material;
    double price;
    double size;
    boolean isWashable;

    Blanket(String brand, String color, String material,
            double price, double size, boolean isWashable) {
        this.brand = brand;
        this.color = color;
        this.material = material;
        this.price = price;
        this.size = size;
        this.isWashable = isWashable;
    }
}