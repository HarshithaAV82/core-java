class Bucket{

    String material;
    String color;
    int capacity;
    double price;
    boolean hasHandle;

    Bucket(String material, String color, int capacity, double price, boolean hasHandle) {
        this.material = material;
        this.color = color;
        this.capacity = capacity;
        this.price = price;
        this.hasHandle = hasHandle;
    }
}